package problem3.node;

public class Node {
    private Object obj;
    private Node next;

    public Node(Object obj) {
        this.obj = obj;
        this.next = null;
    }

    public Object getObj() {
        return obj;
    }

    public void setObj(Object obj) {
        this.obj = obj;
    }

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }
}
